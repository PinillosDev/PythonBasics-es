"""
La suma, la resta, la multiplicación y demás operaciones matemáticas pueden ser llevadas
a cabo en Python. Veámos cómo...
"""


print(4 + 6) # Suma (+)

print(4 - 2) # Resta (-)

print(3 * 3) # Multiplicación (*)

print(28 / 7) # División (puede tener resultado decimal) (/)

print(28 // 7) # División entera (su resultado es siempre un número int) (//)

print(7 ** 7) # Potenciación (**)

print(12 % 5) # Módulo (residuo que queda de una división entera. El residuo de 12 / 5) (%)



