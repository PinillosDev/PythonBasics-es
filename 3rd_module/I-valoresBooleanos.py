"""
Como hemos mencionado anteriormente, bool hace referencia a un tipo de dato el cual solo puede tener dos valores posibles.
Estos valores son True o False (falso o verdadero), pero no hemos mencionado como se trabaja con este tipo de dato que
es muy importantes. En resumen, Bool representa un estado; puede ser sí o no, frío o caliente, 0 o 1
y así con muchos ejemplos.

Los operadores que usaremos para manipular datos bool son los siguientes.
"""



# Creemos estas variables, las cuales usaremos
var1 = 5
var2 = 7



print(var1 == var2) # Igualdad (==) ¿Es esto igual a esto?

print(var1 != var2) # Desigualdad (!=) ¿Es esto diferente a esto?

print(var1 < var2) # Menor que. (<) # ¿Es esto menor que esto?

print(var1 > var2) # Mayor que. (>)

print(var1 <= var2) # Menor o igual que. (<=)

print(var1 >= var2) # Mayor o igual que. (>=)



